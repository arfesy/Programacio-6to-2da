let num = Number(prompt("Numero:"));
if (num % 2 == 0) {
    console.log("Es par");
} else {
    console.log("Es impar");
}

let numero = Number(prompt("Numero:"));
if (numero >= 0) {
    console.log("Es positivo o cero");
} else {
    console.log("Es negativo");
}

let nota = Number(prompt("Nota:"));
if (nota >= 6) {
    console.log("Aprobado");
} else {
    console.log("Desaprobado");
}

let anio = Number(prompt("Año:"));
